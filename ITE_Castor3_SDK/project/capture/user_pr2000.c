void printf_register()
{
	uint8_t reg_table[3][256] = {0};
	int i;
	uint8_t page;
	for(page = 0; page < 3; page ++)
	{
		for(i = 0; i<256; i++)
		{
			pr2000_i2c_write(0xb8,0xff,page);
			usleep(10);
			reg_table[page][i] = pr2000_i2c_read(0xb8,i); //Read 0Page 0xFC.
			usleep(10);
		}
	}
	for(page = 0; page < 3; page ++)
	{
		printf("--------------------->page %d\n",page);
		for(i = 0; i<256; i++)
		{
			printf("->0x%x  0x%x\n",i,reg_table[page][i]);
		}
	}


}
