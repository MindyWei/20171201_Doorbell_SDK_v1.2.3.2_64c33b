uint8_t pr2000_i2c_read(uint8_t slaveAddr,uint8_t regAddr)
{
	uint8_t val;
	mmpIicReceiveData(IIC_PORT_1, IIC_MASTER_MODE, slaveAddr>>1, &regAddr, 1,&val, 1);
	usleep(10);
	return val;
}
void pr2000_i2c_write(uint8_t slaveAddr,uint8_t regAddr,uint8_t val)
{
	mmpIicSendData(IIC_PORT_1, IIC_MASTER_MODE, slaveAddr>>1, regAddr, &val,1);
	usleep(10);
	//mmpIicStop(IIC_PORT_1);
	return;
}

