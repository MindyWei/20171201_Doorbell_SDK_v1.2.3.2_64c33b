void  pr2000_gain(int mode)
{
	if(mode == 1)
		gain_num --;
	else
		gain_num ++;
	if(gain_num < 0)
		gain_num = 0;
	if(gain_num > 31)
		gain_num = 31;
	pr2000_i2c_write(0xb8,0xff,0x00);
	pr2000_i2c_write(  0xb8, 0x12, 0x20);
	pr2000_i2c_write(  0xb8, 0x13, gain_val_s[gain_num]);
	return ;
}

